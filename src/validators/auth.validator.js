/**
 * Auth Validators
 *
 * Joi schemas for validating all auth request bodies.
 *
 * Design decisions:
 *  - email normalized to lowercase at validation time so the service never deals
 *    with mixed-case emails
 *  - password complexity enforced only on registration/reset/change — login
 *    accepts any string so we don't lock out users who registered before a
 *    policy change
 *  - token field accepts any non-empty string up to 2048 chars — tokens are
 *    64-char hex strings from crypto.randomBytes but we stay loose here so
 *    a future change to token format doesn't require a validator update
 *  - name strips leading/trailing whitespace and bans control characters
 *  - all max lengths prevent payload abuse
 *  - stripUnknown is handled by the validate middleware, not here
 */

import Joi from 'joi';

// ---------------------------------------------------------------------------
// Shared field definitions
// ---------------------------------------------------------------------------

const emailField = Joi.string()
    .email({ tlds: { allow: false } })
    .lowercase()
    .trim()
    .max(254)
    .required()
    .messages({
        'string.email': 'Please provide a valid email address.',
        'any.required': 'Email is required.',
        'string.max': 'Email must not exceed 254 characters.',
    });

/**
 * Strong password — enforced on registration, reset, and change.
 * At least: 1 uppercase, 1 lowercase, 1 digit, 1 special char.
 */
const strongPassword = (label = 'Password') =>
    Joi.string()
        .trim()
        .min(8)
        .max(128)
        .required()
        .pattern(/^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*?&.,\-_#^()])[^\s]+$/)
        .messages({
            'any.required': `${label} is required.`,
            'string.min': `${label} must be at least 8 characters.`,
            'string.max': `${label} must not exceed 128 characters.`,
            'string.pattern.base': `${label} must contain at least one uppercase letter, one lowercase letter, one number, and one special character (@$!%*?&.,\\-_#^()).`,
        });

/**
 * Token field — accepts the 64-char hex token generated by crypto.randomBytes(32).
 * Keeping the max generous (512) in case we ever extend token length.
 */
const tokenField = Joi.string()
    .trim()
    .min(32)
    .max(512)
    .required()
    .messages({
        'any.required': 'Token is required.',
        'string.min': 'Invalid token.',
        'string.max': 'Invalid token.',
    });

// ---------------------------------------------------------------------------
// Exported schemas (one per endpoint)
// ---------------------------------------------------------------------------

/** POST /auth/register */
export const register = Joi.object({
    name: Joi.string()
        .trim()
        .min(2)
        .max(100)
        .pattern(/^[^\u0000-\u001F\u007F-\u009F]+$/)  // no control characters
        .required()
        .messages({
            'any.required': 'Name is required.',
            'string.min': 'Name must be at least 2 characters.',
            'string.max': 'Name must not exceed 100 characters.',
            'string.pattern.base': 'Name contains invalid characters.',
        }),
    email: emailField,
    password: strongPassword('Password'),
});

/** POST /auth/login */
export const login = Joi.object({
    email: emailField,
    // No complexity requirement — just ensure it's a non-empty string
    password: Joi.string().trim().max(256).required().messages({
        'any.required': 'Password is required.',
    }),
});

/** POST /auth/verify-email */
export const verifyEmail = Joi.object({
    token: tokenField,
});

/** POST /auth/resend-verification-email */
export const resendVerificationEmail = Joi.object({
    email: emailField,
});

/** POST /auth/forgot-password */
export const forgotPassword = Joi.object({
    email: emailField,
});

/** POST /auth/reset-password */
export const resetPassword = Joi.object({
    token: tokenField,
    password: strongPassword('Password'),
});

/** POST /auth/change-password  (authenticated) */
export const changePassword = Joi.object({
    currentPassword: Joi.string().trim().max(256).required().messages({
        'any.required': 'Current password is required.',
    }),
    newPassword: strongPassword('New password'),
}).custom((value, helpers) => {
    if (
        value.currentPassword &&
        value.newPassword &&
        value.currentPassword === value.newPassword
    ) {
        return helpers.error('any.invalid', { label: 'newPassword' });
    }
    return value;
}).messages({
    'any.invalid': 'New password must be different from your current password.',
});